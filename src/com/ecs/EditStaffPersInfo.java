    private void validateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validateActionPerformed
        if(staffId.getText().equals("") ){
            JOptionPane.showMessageDialog(this,"Empty fields", "Empty fields", JOptionPane.OK_OPTION);
        }else{
            try{
                String sql1= "select * from staff_pers_info where staff_id="+staffId.getText()+"";
                DBConnection.rs= DBConnection.st.executeQuery(sql1);
                if(DBConnection.rs.next()){
                    firstname.setText(""+DBConnection.rs.getString("fname"));
                    middlename.setText(DBConnection.rs.getString("mname"));
                    surname.setText(DBConnection.rs.getString("lname"));
                    mstatus.setSelectedItem(""+DBConnection.rs.getString("mstatus"));
                    gender.setSelectedItem(""+DBConnection.rs.getString("gender"));
                    religion.setSelectedItem(DBConnection.rs.getString("religion"));
                    String me= DBConnection.rs.getString("passport");
                    Icon iicon= new ImageIcon(me);
                    me1.setIcon(iicon);
                    dob.setDate(new java.util.Date(DBConnection.rs.getString("dob").replaceAll("-", "/")));
                    
                    staffId.setEnabled(false);
                    validate.setEnabled(false);
                    update.setEnabled(true);
                }else{
                    JOptionPane.showMessageDialog(this, "Staff ID does not exist",
                        "Staff not found", JOptionPane.OK_OPTION);
                }
            }catch(Exception ex){
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_validateActionPerformed

