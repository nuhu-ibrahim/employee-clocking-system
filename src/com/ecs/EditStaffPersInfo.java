    private void selectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int returnValue;
        returnValue = fileChooser.showOpenDialog(this);
        if(returnValue== JFileChooser.APPROVE_OPTION){
            try{
                File file;
                file= fileChooser.getSelectedFile();
                String st= file.toString();
                int index=st.lastIndexOf('.');
                String test=st.substring(index+1);
                if(test.equals("jpg")){
                    Icon iicon= new ImageIcon(file.getPath());
                    int a= iicon.getIconHeight();
                    int b= iicon.getIconWidth();
                    if(a<=150 && b<=150){
                        me1.setIcon(iicon);
                    }else{
                        me1.setIcon(null);
                        JOptionPane.showMessageDialog(null, "Image must not be larger than 150px X 150px","Issue!", JOptionPane.OK_OPTION);
                    }
                }else{
                    me1.setIcon(null);
                    JOptionPane.showMessageDialog(null, "File is not .jpg format","Issue!", JOptionPane.OK_OPTION);
                }

            }catch(Exception ex){
                me1.setIcon(null);
            }

        }
    }//GEN-LAST:event_selectActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if(staffId.getText().equals("") || firstname.getText().equals("") ||
                mstatus.getSelectedIndex()==0 || surname.getText().equals("")
                || dob.getDate() == null || surname.getText().equals("")
                || gender.getSelectedIndex()==0 || religion.getSelectedIndex()==0 || ""+me1.getIcon()==""){
            JOptionPane.showMessageDialog(this,"Empty fields", "Empty fields", JOptionPane.OK_OPTION);
        }else{
            try{
                SimpleDateFormat ft = new SimpleDateFormat("yyyy-MM-dd");
                String date = ft.format(dob.getDate());
                
               
                PreparedStatement st;
                
                String sql= "update staff_pers_info set lname=?, mname=?, fname=?, mstatus=?, gender=?, dob=?, religion=?, passport=? where staff_id=?";
                st = DBConnection.con.prepareStatement(sql);
                
                st.setString(1, surname.getText());
                st.setString(2, middlename.getText());
                st.setString(3, firstname.getText());
                st.setString(4, ""+mstatus.getSelectedItem());
                st.setString(5, ""+gender.getSelectedItem());
                st.setString(6, date);
                st.setString(7, ""+religion.getSelectedItem());
                st.setString(8, ""+me1.getIcon());
                st.setInt(9, Integer.parseInt(staffId.getText()));
                
                st.executeUpdate();
                
                JOptionPane.showMessageDialog(this,"Staff Information Updated",
                    "Updated", JOptionPane.INFORMATION_MESSAGE);
                refresh();
            }catch(Exception ex){
                ex.printStackTrace();
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_updateActionPerformed

